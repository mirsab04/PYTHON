#Method Of String
a = 'abcd'
b = 'abd123'
c = '12345'
d = 'ab cd'

#CHECK ALPHA METHOD
print(a.isalpha())
print(b.isalpha())
print(c.isalpha())
#CHECK DIGIT METHOD
print(a.isdigit())
print(b.isdigit())
print(c.isdigit())
#CHECK BOTH APLHA AND DIGIT METHOD
print(a.isalnum())
print(b.isalnum())
print(c.isalnum())
print(d.isalnum())
